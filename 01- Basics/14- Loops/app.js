// While loop

i = 5;
while (i >= 0) {
  console.log("value of i is ", i);
  i--;
}

// do while loop = code block first, condition second. It will always run atleast once even if the condition is false.

j = 10;
do {
  console.log("Hello World");
} while (j > 10);

// for loop
let n;
for (n = 0; n <= 10; n++) {
  console.log(n);
}
