// (+), (-), (/), (*), (%)
const num1 = 10;
const num2 = 25;

const add = num1 + num2;
const diff = num1 - num2;
const prod = num1 * num2;
const div = num2 / num1;
const remainder = num2 % num1;

console.log(add);
console.log(diff);
console.log(prod);
console.log(div);
console.log(remainder);

// +=, -=, *=, /=, ++, --

let val = 10;
val += 5;
console.log(val);
val -= 10;
console.log(val);
val *= 5;
console.log(val);
val /= 5;
console.log(val);

// ++, --
val--;
console.log(val);
val++;
console.log(val);

